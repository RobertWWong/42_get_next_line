int		ft_strcount(char const *s, char c)
{
	int	num;

	if (!(*s))
		return (0);
	num = (*s == c) ? 0 : 1;
	while (*s)
	{
		if (*s == c)
		{
			while (*s == c)
				s++;
			num++;
		}
		else
			s++;
	}
	if (*(s - 1) == c)
		num--;
	return (num);
}
